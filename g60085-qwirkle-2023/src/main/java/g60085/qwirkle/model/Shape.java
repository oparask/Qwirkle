package g60085.qwirkle.model;

/**
 * Represents the shape of a tile in the Qwirkle game.
 * <p>
 * The shape enum defines six shapes: CROSS, SQUARE, ROUND, STAR, PLUS and DIAMOND;
 * Each shape is used to represent a specific attribute of a tile in the game.
 * </p>
 */
public enum Shape {
    CROSS, SQUARE, ROUND, STAR, PLUS, DIAMOND;
}
